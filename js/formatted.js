function display(line) { // Transforms data from a raw object into a formatted string
	var displayStr = String(line); 
	var str = displayStr.split(","); //Bye, bye commas!
	var displaytext = "";
	var i;
	for (i = 0; i < str.length; i++) {
		displaytext += str[i] + ", ";
		}
		//alert(displaytext);
	return displaytext;
}

